    void displayTable(){
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) table1.getModel();
            int r = dtm.getRowCount();
            
            while (r-- > 0){
                dtm.removeRow(0);
            }
            
            java.time.LocalDate cd = java.time.LocalDate.now();
            java.time.LocalDate bd = cd.minusDays(20);
            ResultSet rs = db.DatabaseHelper.s.executeQuery("select * from spending_info where sdate<='"+cd+"' and sdate>='"+bd+"'");
            int total = 0;
            while (rs.next()){
                int sid = rs.getInt("sid");
                java.util.Date d = rs.getDate("sdate");
                int amt = rs.getInt("Amount");
                total += amt;
                String category = rs.getString("category");
                      
                java.util.Vector v = new java.util.Vector();
                v.add(sid);
                v.add(d);
                v.add(category);
                v.add(amt);
                
                dtm.addRow(v);
            }
            label7.setText(Integer.toString(total));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    void displayCategory(){
        try{
            combo1.removeAllItems();
            ResultSet rs = db.DatabaseHelper.s.executeQuery("select * from category_info");
            
            while (rs.next()){
                combo1.addItem(rs.getString("category"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

