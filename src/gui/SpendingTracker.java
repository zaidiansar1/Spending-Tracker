    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            java.util.Date d = date.getDate();
            String t = t1.getText();
            String c = (String)combo1.getSelectedItem();
            
            if(d != null && !t.isEmpty() && !c.isEmpty()){
                java.sql.Date date = new java.sql.Date(d.getTime());
                db.DatabaseHelper.s.executeUpdate("insert into spending_info (category,sdate,Amount) values('"+c+"','"+date+"',"+t+")");
                displayTable();
            }
            else{
                JOptionPane.showMessageDialog(null, "Please enter the required fields!!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int ri = table1.getSelectedRow();
        int id = (int) table1.getValueAt(ri, 0);
        
        try{
            int r = JOptionPane.showConfirmDialog(null, "Are you Sure!!");
            if(r == JOptionPane.YES_OPTION){
                db.DatabaseHelper.s.executeUpdate("delete from spending_info where sid="+id+"");
                displayTable();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please Select item!!!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

