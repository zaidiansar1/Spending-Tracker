    void displayCategory(){
        combo.removeAllItems();
        try{
            ResultSet rs = db.DatabaseHelper.s.executeQuery("select * from category_info");
            
            while (rs.next()){
                combo.addItem(rs.getString("category"));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid!!");
        }
    }

