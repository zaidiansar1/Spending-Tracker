    void getEnteries1(){
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) table1.getModel();
            
            int cnt = dtm.getRowCount();
            
            while (cnt-- > 0){
                dtm.removeRow(0);
            }
            
            java.util.Date date1 = d1.getDate();
            java.util.Date date2 = d2.getDate();
            
            if (date1 != null && date2 != null){
                java.sql.Date d11 = new java.sql.Date(date1.getTime());
                java.sql.Date d12 = new java.sql.Date(date2.getTime());
                
                ResultSet rs = db.DatabaseHelper.s.executeQuery("select * from spending_info where sdate<='"+d12+"' and sdate>='"+d11+"'");
                int s = 0;
                while (rs.next()){
                    s += rs.getInt("amount");
                    Object[] o = {rs.getDate("sdate"), rs.getInt("amount"), rs.getString("category")};
                    dtm.addRow(o);
                }
                l1.setText("$"+Integer.toString(s));
            }
            else{
                JOptionPane.showMessageDialog(null, "Please enter details!!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
